print("Você quer 'entrar' ou 'sair'?")
entrada = input()

if entrada == 'entrar':
    print('Você entrou no sistema')
    
    print("Parabéns :)")
elif entrada == 'sair':
    print('Você saiu do sistema')
    
    print("Que pena! :(")
else:
    print('Você não digitou nem entrar nem sair.')

